-- Create profiles table if it doesn't exist
create table if not exists public.profiles (
  id uuid references auth.users not null primary key,
  updated_at timestamp with time zone,
  full_name text,
  email text,
  phone text,
  city text,
  state text,
  country text,
  linkedin_url text,
  github_url text,
  portfolio_url text,
  current_company text,
  current_title text,
  resume_path text,
  race text,
  veteran_status text,
  disability_status text,
  primary_role text
);

-- Add columns if they don't exist (for existing tables)
do $$
begin
  if not exists (select 1 from information_schema.columns where table_name = 'profiles' and column_name = 'race') then
    alter table public.profiles add column race text;
  end if;
  if not exists (select 1 from information_schema.columns where table_name = 'profiles' and column_name = 'veteran_status') then
    alter table public.profiles add column veteran_status text;
  end if;
  if not exists (select 1 from information_schema.columns where table_name = 'profiles' and column_name = 'disability_status') then
    alter table public.profiles add column disability_status text;
  end if;
  if not exists (select 1 from information_schema.columns where table_name = 'profiles' and column_name = 'primary_role') then
    alter table public.profiles add column primary_role text;
  end if;
end $$;

-- Create experiences table
create table if not exists public.autofill_experiences (
  id bigint generated by default as identity primary key,
  user_id uuid references auth.users not null,
  company_name text,
  job_title text,
  location text,
  start_month text,
  start_year integer,
  end_month text,
  end_year integer,
  is_current boolean,
  description text,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- Create educations table
create table if not exists public.autofill_educations (
  id bigint generated by default as identity primary key,
  user_id uuid references auth.users not null,
  school_name text,
  degree_type text,
  major text,
  start_month text,
  start_year integer,
  end_month text,
  end_year integer,
  description text,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- Create skills table
create table if not exists public.autofill_skills (
  id bigint generated by default as identity primary key,
  user_id uuid references auth.users not null,
  skill_name text,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- Create languages table
create table if not exists public.autofill_languages (
  id bigint generated by default as identity primary key,
  user_id uuid references auth.users not null,
  language_name text,
  proficiency text,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

