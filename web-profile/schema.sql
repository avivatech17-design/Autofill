-- Storage Policies (for 'resumes' bucket)
-- Allow authenticated users to upload files to the 'resumes' bucket
drop policy if exists "Users can upload own resume" on storage.objects;
create policy "Users can upload own resume" on storage.objects for insert with check (
  bucket_id = 'resumes' and
  auth.role() = 'authenticated' and
  (storage.foldername(name))[1] = auth.uid()::text
);

-- Allow authenticated users to view their own resume
